<h1>EYFS</h1>
<h2>Linking to Early Years Foundation Stages</h2>
All early years settings are required by Ofsted to track children's progress and development in line with the EYFS. The At Nursery Today app has been developed to support this on many levels.
<ol>
	<li>To make life easy for it's users, the app has been programmed to include all of the development statements from the EYFS to enable easy links to be made between activities and children's developmental progress</li>
	<li>It can provide parents/carers with brief updates on their child's achievements and a record of dates of when milestones are met.</li>
	<li>The themes of Development Matters are included to provide a holistic approach to tracking each individual child.</li>
	<li>Photographs and general observations can be added and can be linked with the EYFS statements.</li>
</ol>
<h2>Parental Feedback and Searching</h2>

<p>The optional parents' website allows parents/carers to add home observations which avoids the need for separate paper forms and keeps all information in one place making it easy to search for desired information.</p>

<p>The staff administration website allows easy searching of EFYS information for a given child, with filtering by days, prime or specific areas and the ages and development statements.</p>

<p>Data can be viewed on the website or downloaded in batch into text files for easy copy and paste into your own children's development trackers etc.</p>
