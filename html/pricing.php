<h2 class="belowTitle">Tablets</h2>
<p>We don't provide the tablets - it is up to nurseries to buy these and insure them but we can recommend some. Obviously many nurseries will already have Android tablets so there won't be any cost.</p>

<p>
The standard tablets we recommend are the 10 inch Samsung Galaxy Tab 4 (&pound;200) or the Google Nexus 9 (&pound;300).
</p>
<p>
There are cheaper Android tablets available which can be used instead but you will usually sacrifice camera quality when going for &pound;100 - &pound;150 tablets so this depends on how many photos you expect to take each day of activities. Be aware that the app is designed to run on large tablets with 9 or 10 inche screen sizes so smaller tablets like the Nexus 7 are not suitable.
</p>


