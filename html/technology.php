<h2 class="belowTitle">What you need</h2>
All you need to use our system is an android based tablet, an internet connection and a laptop/PC and we can help with setting everything up.

We provide the tablets unless you already have ones you want to use. A fixed broadband connection is ideal but if this isn't an option then 3G or 4G mobile broadband is ok too. The PC is for administration tasks like adding children and their parents emails to the system. If you don't have one then it is possible to use the tablet for this task too.
<h2>System Overview</h2>
<img class="imageByTextBigAboveSm" src="/images/systemoverview.png" alt="systemoverview" /></a>

Daily report information is entered on the tablets each day. At the end of the day that information is uploaded to our servers which process that information and send out emails to the parents. Parents can also log onto our website and browse their child's previous days at nursery including a gallery of any photos taken.
<h2>Administration</h2>
In order to get a child's information onto the system in the first place an administration website is provided. This lets you:
<ul>
	<li>Add children (and their photo if you want)</li>
	<li>Assign children to a room/tablet</li>
	<li>Configure tablets - e.g. accounts and what will be in a daily report</li>
	<li>Upload attachments that can be sent to parents with daily reports</li>
</ul>
<h2>Security</h2>
Security has been designed into the system from the beginning to ensure that each child's information is safe. Neither other parents nor even nursery staff can browse a childs information once uploaded. All services are password protected and encryption is used for all transfer to our servers.

