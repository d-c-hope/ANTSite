		<h1>Introduction</h1>
		<p>
			At Nursery Today (ANT) is a unique app which provides parents/carers with an automatically emailed daily report on their child's day at nursery. Staff use modern computer tablets to generate these paperless daily records.
		</p>
		<p>
		It brings benefits for both parents/carers and the nursery. Parents can find out what their child ate for lunch, when they slept or look at photos of their child's art or activities.
		</p>
		<p>
		Nursery staff can keep a record of children's progress and activities for their own records and progress tracking. All they need to do is use our simple Android tablet app to record as little or as much as they like over the course of the day for each child. </p>
		<p>This is similar to a traditional paper daily record sheet or child diaries but makes form filling much easier and more consistent than paper alternatives.
		</p>
		<h2>Features</h2>
		<ul>
			<li>Staff use our, simple, secure tablet app for providing detailed reports on each child's day such as activities, sleep, meals, notifications etc.</li>
			<li>A child's recorded activities can be linked to the Early Years Foundation Stage - EYFS.</li>
			<li>The information entered by staff is automatically generated into a well written report which is emailed to parents/ carers.</li>
			<li>A secure, password proected parental website also allows viewing of reports and photos</li>
			<li>Easy administration website for nursery staff to add children and customise reports</li>
			<li>For larger settings or where children spend time in more than 1 room, information can be shared between tablets.</li>
		</ul>

